export default class FilmEditor {
  static editTitle(film, newTitle) {
    film.title = newTitle;
  }

  static editGenre(film, newGenre) {
    film.genre = newGenre;
  }

  static editDuration(film, newDuration) {
    film.duration = newDuration;
  }

  static editDirector(film, newDirector) {
    film.director = newDirector;
  }

  static editReleaseYear(film, newReleaseYear) {
    film.releaseYear = newReleaseYear;
  }

  static editRating(film, newRating) {
    film.rating = newRating;
  }

  static editSynopsis(film, newSynopsis) {
    film.synopsis = newSynopsis;
  }

  static editCast(film, newCast) {
    film.cast = newCast;
  }
  static editCoverImage(film, newCoverImage) {
    film.coverImage = newCoverImage;
  }
}




